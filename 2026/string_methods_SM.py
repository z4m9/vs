user = input("What is your favourite saying? ") # User prompt
upper = user.upper() # Capitalises saying
lower = user.lower() # Saying is fully lowercased
print(f"Normal version: {user}") # Normal, unchanged version
print(f"Uppercase version: {upper}") # Capitalised version
print(f"Lowercase version: {lower}") # All lowercase version